////////////////////////////////////////////////////////////////////////////////////////////////////
/// <summary>	Set the location of the cow at x=200 and y=600 on screen.
/// 			Set the cow's velocity to 0. </summary>
///
/// <remarks>	Andrew Servania,. </remarks>
///
/// <param name="cow">	[in,out] If non-null, the cow. </param>
////////////////////////////////////////////////////////////////////////////////////////////////////

void CowGlobalState::Execute(Cow* cow)
{
	cow->SetPos(Vector2D(200, 600));
	cow->SetVelocity(Vector2D(0, 0));
}

