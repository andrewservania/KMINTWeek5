////////////////////////////////////////////////////////////////////////////////////////////////////
/// <summary>	Take action right after entering the state. </summary>
///
/// <remarks>	Andrew Servania,. </remarks>
///
/// <param name="rabbit">	[in,out] If non-null, the rabbit. </param>
////////////////////////////////////////////////////////////////////////////////////////////////////

void RabbitGlobalState::Enter(Rabbit* rabbit)
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////
/// <summary>	The rabbit is put at x=600 and y=600 on the arena.
/// 			The rabbit's velocity is set to zero.
///
/// 			The rabbit will do nothing while being in a Global state </summary>
///
/// <remarks>	Andrew Servania,. </remarks>
///
/// <param name="rabbit">	[in,out] If non-null, the rabbit. </param>
////////////////////////////////////////////////////////////////////////////////////////////////////

void RabbitGlobalState::Execute(Rabbit* rabbit)
{
	rabbit->SetPos(Vector2D(600, 600));
	rabbit->SetVelocity(Vector2D(0, 0));
}

